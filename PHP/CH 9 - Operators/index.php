// Arithmetic Operators (+. -, /. *. %, and **)
    $num1 = 10;
    $num2 = 25;

    # Addition Operators
    echo $num1 + $num2 . '<br>';

    # Subtraction Operators
    echo $num2 - $num1 . '<br>';

    # Multiplication Operators
    echo $num1 * $num2 . '<br>';

    # Division Operators
    echo $num2 / $num1 . '<br>';

    # Modulus Operators
