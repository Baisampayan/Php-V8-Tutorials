    $num1 = 15;
    echo $num1 . '<br>';

    # Addition Assignment Operators
    echo ($num1 + $num2) . '<br>';
    echo ($num1 += $num2) . '<br>';

    # Subtraction Assignment Operators
    echo ($num1 - $num2) . '<br>';
    echo ($num1 -= $num2) . '<br>';

    # Multiplication Assignment Operators
    echo ($num1 * $num2) . '<br>';
    echo ($num1 *= $num2) . '<br>';

    # Division Assignment Operators
    echo ($num1 / $num2) . '<br>';
    echo ($num1 /= $num2) . '<br>';

    # Modulus Assignment Operators
    echo ($num2 % $num1) . '<br>';
    echo ($num2 %= $num1) . '<br>';

